async def tts_command(message: discord.Message):
    text = " ".join(message.content.split()[1:])

    if not text:
        embed = discord.Embed(
            title="Missing arguments",
            description=f"Please make sure you pass some text for the TTS command. Usage: {BOT_PREFIX}tts [message]",
            color=0xf55c3d,
        )
